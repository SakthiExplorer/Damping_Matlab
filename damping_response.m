zeta1=0.2;
zeta2=0;
zeta3=1;
zeta4=5;
phi1=acos(zeta1);
wn=0.3;
wd1=wn*sqrt(1-(zeta1*zeta1));
wd2=wn*sqrt(1-(zeta2*zeta2));
wd3=wn*sqrt(1-(zeta3*zeta3));
wd4=wn*sqrt(1-(zeta4*zeta4));
t=0:1:100;
a1=zeta1*wn;
a2=zeta2*wn;
a3=zeta3*wn;
a4=zeta4*wn;
c1=exp((-1)*a1*t);
c2=sqrt(1-(zeta1*zeta1));
c3=sin(wd1*t+phi1);
c4=sqrt((zeta4*zeta4)-1);
c5=(zeta4-c4)*wn*t;
c6=(zeta4+c4)*wn*t;
c7=(exp((-1)*c5))/(2*c4*(zeta4-c4));
c8=(exp((-1)*c6))/(2*c4*(zeta4+c4));
c11=1-(c1.*c3/c2); 
c12=1-sin(wn*t+((1*pi)/2)); 
c13=1-((exp((-1)*wn*t)).*(1-(wn*t)));
c14=1-(c7-c8);
subplot(1,1,1);
plot(t,c11,'b');
title("Damping response");
xlabel("time");
ylabel("output");
hold on;
plot(t,c12,'r');
plot(t,c13,'m');
plot(t,c14,'g');
legend('underdamp','undamp','critically damp','overdamp');
hold off
